class Post(db.Model):
    __tablename__ = 'posts'
    id = db.Column(db.Integer, primary_key=True)
    body = db.Column(db.Text)
    title = db.Column(db.String(60))
    body_html = db.Column(db.Text)
    timestamp = db.Column(db.DateTime, index=True, default=datetime.utcnow())
    author_id = db.Column(db.Integer, db.ForeignKey('users.id'))
    comments = db.relationship('Comment', backref='post', lazy='dynamic', cascade='all, delete-orphan')
    category_id = db.Column(db.Integer, db.ForeignKey('categories.id'))
    category = db.relationship('Category', back_populates='posts')
    can_comment = db.Column(db.Boolean, default=True)

    @staticmethod
    def generate_fake_title():
        from forgery_py import lorem_ipsum

        for p in Post.query.all():
            p.title = lorem_ipsum.title()
            db.session.add(p)
            db.session.commit()

    @staticmethod
    def generate_fake_category(count=202):
        import random

        for i in range(1, count):
            p = Post.query.filter_by(id=i).first()
            if p is not None:
                p.category_id = random.randint(1, 10)
                db.session.add(p)
                db.session.commit()

    @staticmethod
    def generate_fake(count=10):
        from random import seed, randint
        from faker import Faker
        import forgery_py

        seed()
        fake = Faker()
        user_count = User.query.count()
        for i in range(count):
            u = User.query.offset(randint(0, user_count - 1)).first()
            p = Post(body=fake.text(2000),
                     title=fake.sentence(),
                     category=Category.query.get(randint(1, Category.query.count())),
                     timestamp=forgery_py.date.date(True),
                     author=u)
            db.session.add(p)
            db.session.commit()

    def to_json(self):
        json_post = {
            'url': url_for('api.get_post', id=self.id, _external=True),
            'body': self.body,
            'body_html': self.body_html,
            'timestamp': self.timestamp,
            'author_url': url_for('api.get_user', id=self.author_id, _external=True),
            'comments_url': url_for('api.get_post_comments', id=self.id, _external=True),
            'comment_count': self.comments.count()
        }
        return json_post

    @staticmethod
    def from_json(json_post):
        body = json_post.get('body')
        if body is None or body == '':
            raise ValidationError('post does not have a body')
        return Post(body=body)

    @staticmethod
    def on_change_body(target, value, oldvalue, initiator):
        allowed_tags = ['a', 'abbr', 'acronym', 'b', 'blockquote', 'code',
                        'em', 'i', 'li', 'ol', 'pre', 'strong', 'ul',
                        'h1', 'h2', 'h3', 'p']
        body_html = markdown(value, output_format='html')
        body_html = bleach.clean(body_html, tags=allowed_tags, strip=True)
        body_html = bleach.linkify(body_html)
        target.body_html = body_html

db.event.listen(Post.body, 'set', Post.on_change_body)


